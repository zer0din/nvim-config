-- Клавиатурные привязки, не зависящие от плагинов.

-- Очистка подстветки поиска (Esc).
vim.keymap.set('n', '<Esc>', '<cmd>nohlsearch<CR>', { desc = 'Очистить подстветку поиска' })

-- Выход из режима ввода встроенного терминала (двойное нажатие Esc).
-- Стандартная комбинация клавишь для этого - Ctrl-\ Ctrl-n.
vim.keymap.set('t', '<Esc><Esc>', '<C-\\><C-n>', { desc = 'Выйти из режима ввода терминала' })

-- Переключатели часто используемых опций.
-- Работают через Leader-клавишу и t (от toogle) плюс конкретная клавиша переключателя.
-- Например:
-- <leader>tw - включает\отключает перенос (wrap) для текста.
-- <leader>ts - включает\отключает проверку орфограции (spell).

-- Включение\отключение проверки орфографии (<leader>ts).
vim.keymap.set('n', '<leader>ts',
	function()
		vim.opt_local.spell = not vim.opt_local.spell:get()
	end,
	{ desc = 'Вкючить\\отключить проверку орфографии' }
)

-- Включение\отключение переноса слов (<leader>tw).
vim.keymap.set('n', '<leader>tw',
	function()
		vim.opt_local.wrap = not vim.opt_local.wrap:get()
	end,
	{ desc = 'Включить\\выключить перенос текста' }
)
